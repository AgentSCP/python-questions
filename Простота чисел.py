i = int(input("Введите число"))
if i % 2 == 0:
    if i == 2:
        print("Верно")
    else:
        print("Неверно")
elif i % 3 == 0:
    if i == 3:
        print("Верно")
    else:
        print("Неверно")
elif i % 5 == 0:
    if i == 5:
        print("Верно")
    else:
        print("Неверно")
elif i % 7 == 0:
    if i == 7:
        print("Верно")
    else:
        print("Неверно")
else:
    print("Верно")